/*
* Flexslider 2参数使用详解
 　　animation 动画效果类型，有"fade"：淡入淡出，"slide"：滑动"fade"
 　　easing 内容切换时缓动效果，需要jquery easing插件支持"swing"
 　　direction 内容滚动方向，有"horizontal"：水平方向 和"vertical"：垂直方向"horizontal"
 　　animationLoop 是否循环滚动true
 　　startAt 初始滑动时的起始位置，定位从第几个开始滑动0
 　　slideshow 是否自动滑动true
 　　slideshowSpeed 滑动内容展示时间（ms）7000
 　　animationSpeed 内容切换时间（ms）600
 　　initDelay 初始化时延时时间0
 　　pauseOnHover 鼠标滑向滚动内容时，是否暂停滚动false
 　　touch 是否支持触摸滑动true
 　　directionNav 是否显示左右方向箭头按钮true
 　　keyboard 是否支持键盘方向键操作true
 　　minItems 一次最少展示滑动内容的单元个数1
 　　maxItems 一次最多展示滑动内容的单元个数0
 　　move一次滑动的单元个数0
* */
